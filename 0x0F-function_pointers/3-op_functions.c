#include "3-calc.h"

/**
 * op_add - add function
 * @a: int to add
 * @b: int to add
 * Return: a+b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub function
 * @a: int to sub
 * @b: int to sub
 * Return: a-b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply function
 * @a: int to mul
 * @b: int to mul
 * Return: a*b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divition function
 * @a: int to div
 * @b: int to div
 * Return: a/b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: int to mod
 * @b: int to mod
 * Return: a%b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
